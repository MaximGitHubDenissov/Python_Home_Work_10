def addition_func(x,y):
    result = round(float(x)+float(y),2)
    return result

def subtrac_func(x,y):
    result = round(float(x)-float(y),2)
    return result

def mult_func(x,y):
    result = round(float(x)*float(y),2)
    return result

def devis_func(x,y):
    result = round(float(x)/float(y),2)
    return result